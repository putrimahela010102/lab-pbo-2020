public class Pegawai2 
{  
	private String nama;  
	private double gaji;    

	public Pegawai2(String nama, double gaji)  
	{   
		this.nama = nama;   
		this.gaji = gaji;  
	}    

	public void setNama(String nama)  
	{   
		this.nama = nama;  
	}    

	public String getNama()  
	{   
		return this.nama;  
	}    

	public void setGaji(double gaji)  
	{   
		this.gaji = gaji;  
	}    

	public double getGaji()  
	{   
		return this.gaji;  
	} 
}